// 1. TRANSLATION ARCHIVE
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

/**
 * One row per translation event captured from the editor.
 *
 * Notes:
 *  - `translatedText` is the primary payload (always Russian per product spec,
 *    but the column stays generic so we can broaden languages later).
 *  - `originalText` is kept for context; nullable to allow user-created
 *    archive entries with no paired original.
 *  - `sourceLanguage`/`targetLanguage` use ISO codes (e.g. "en", "ru").
 */
@Entity(
    tableName = "analytics_translations",
    indices = [
        Index(value = ["created_at"]),
        Index(value = ["updated_at"]),
        Index(value = ["target_language"]),
        Index(value = ["source_record_id"])
    ]
)
data class AnalyticsTranslationEntity(
    @PrimaryKey(autoGenerate = true)
    @ColumnInfo(name = "id")
    val id: Long = 0,

    @ColumnInfo(name = "translated_text")
    val translatedText: String,

    @ColumnInfo(name = "original_text")
    val originalText: String? = null,

    @ColumnInfo(name = "source_language", defaultValue = "'auto'")
    val sourceLanguage: String = "auto",

    @ColumnInfo(name = "target_language", defaultValue = "'ru'")
    val targetLanguage: String = "ru",

    /**
     * Denormalized trace fields вЂ” set when the entry was mirrored from a
     * real document. Nullable because user can also create entries manually.
     */
    @ColumnInfo(name = "source_document_id")
    val sourceDocumentId: Long? = null,

    @ColumnInfo(name = "source_record_id")
    val sourceRecordId: Long? = null,

    @ColumnInfo(name = "source_record_name")
    val sourceRecordName: String? = null,

    @ColumnInfo(name = "source_folder_name")
    val sourceFolderName: String? = null,

    @ColumnInfo(name = "user_modified", defaultValue = "0")
    val userModified: Boolean = false,

    @ColumnInfo(name = "word_count", defaultValue = "0")
    val wordCount: Int = 0,

    @ColumnInfo(name = "created_at", defaultValue = "0")
    val createdAt: Long = 0L,

    @ColumnInfo(name = "updated_at", defaultValue = "0")
    val updatedAt: Long = 0L
)

/**
 * FTS4 mirror for global search.
 *
 * Uses `content=AnalyticsTranslationEntity` so the FTS table doesn't duplicate
 * storage вЂ” it just references the source table by rowid.
 *
 * Sync triggers are created in MIGRATION_20_21.
 */
@Entity(tableName = "analytics_translations_fts")
@Fts4(contentEntity = AnalyticsTranslationEntity::class)
data class AnalyticsTranslationFtsEntity(
    @ColumnInfo(name = "translated_text")
    val translatedText: String,

    @ColumnInfo(name = "original_text")
    val originalText: String?
)

