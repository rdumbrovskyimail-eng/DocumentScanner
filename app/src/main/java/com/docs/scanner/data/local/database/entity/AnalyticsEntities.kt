// 2. NOTES (Information Analysis)
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

/**
 * Free-form note. Title + content + tags + colour swatch.
 *
 *  - `tags` is a JSON string array (re-using the same convention as records).
 *  - `color` is an optional ARGB hex string ("#FF8E24AA") consumed by the UI.
 *  - `isPinned` keeps a note at the top of the list.
 *  - `isArchived` hides a note from the default list but keeps it for restore.
 */
@Entity(
    tableName = "analytics_notes",
    indices = [
        Index(value = ["updated_at"]),
        Index(value = ["created_at"]),
        Index(value = ["is_pinned"]),
        Index(value = ["is_archived"])
    ]
)
data class AnalyticsNoteEntity(
    @PrimaryKey(autoGenerate = true)
    @ColumnInfo(name = "id")
    val id: Long = 0,

    @ColumnInfo(name = "title", defaultValue = "''")
    val title: String = "",

    @ColumnInfo(name = "content", defaultValue = "''")
    val content: String = "",

    /** JSON array of plain strings, e.g. `["analysis","draft"]`. Use TagsJson helpers. */
    @ColumnInfo(name = "tags", defaultValue = "'[]'")
    val tags: String = "[]",

    @ColumnInfo(name = "color")
    val color: String? = null,

    @ColumnInfo(name = "is_pinned", defaultValue = "0")
    val isPinned: Boolean = false,

    @ColumnInfo(name = "is_archived", defaultValue = "0")
    val isArchived: Boolean = false,

    @ColumnInfo(name = "created_at", defaultValue = "0")
    val createdAt: Long = 0L,

    @ColumnInfo(name = "updated_at", defaultValue = "0")
    val updatedAt: Long = 0L
)

/**
 * FTS4 mirror for global search across notes.
 */
@Entity(tableName = "analytics_notes_fts")
@Fts4(contentEntity = AnalyticsNoteEntity::class)
data class AnalyticsNoteFtsEntity(
    @ColumnInfo(name = "title")
    val title: String,

    @ColumnInfo(name = "content")
    val content: String
)